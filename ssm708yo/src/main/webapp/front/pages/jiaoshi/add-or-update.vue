<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				jiaoshigonghao: '',
				mima: '',
				jiaoshixingming: '',
				zhaopian: '',
				xingbie: '',
				zhicheng: '',
				lianxidianhua: '',
				suojiaokecheng: '',
				},
				xingbieOptions: [],
				xingbieIndex: 0,
				zhichengOptions: [],
				zhichengIndex: 0,
				suojiaokechengOptions: [],
				suojiaokechengIndex: 0,
				// 登陆用户信息
